'''
Fazer um algoritmo que pergunte dois números e ao final apresente os seguintes valores: a soma dos dois
números, a subtração do primeiro pelo segundo número, a subtração do segundo pelo primeiro número, a
multiplicação entre os dois números, a divisão do primeiro pelo segundo número, e também o resto da divisão
do primeiro pelo segundo número.
'''

a = int(input("Insira um número: "))
b = int(input("Insira outro número: "))

print("A soma dos dois números é igual a", a + b, ", a subtração do primeiro pelo segundo número é igual a ", a - b, ", a subtração do segundo pelo primeiro número é igual a", b - a, ", a multiplicação entre os dois números é igual a", a * b, ", a divisão do primeiro pelo segundo número é igual a", a / b, "o resto da divisão do primeiro pelo segundo número é igual a", a % b, "." )